using System;
using System.Threading.Tasks;
using Covi.Features.Exceptions;
using Covi.Services.Notification;

namespace Covi.Services.ErrorHandlers
{
    /// <summary>
    /// Handles errors generated by application layer.
    /// </summary>
    public class DomainErrorHandler : IErrorHandler
    {
        private readonly INotificationManager _notificationManager;

        public DomainErrorHandler(INotificationManager notificationManager)
        {
            _notificationManager = notificationManager;
        }

        public async Task<bool> HandleAsync(Exception error)
        {
            if (error is IUserFriendlyException userFriendlyException)
            {
                await _notificationManager.ShowNotificationAsync(
                    Resources.Localization.ErrorDialog_Title,
                    userFriendlyException.Message,
                    Resources.Localization.Dialog_Accept);
                return true;
            }

            return false;
        }
    }
}
